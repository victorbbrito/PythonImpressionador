def soma_3(num1: int,num2: int,num3: int):
    '''Faz a soma de 3 números interios e retorna como resposta um inteiro
    
    Parameter:
        num1 (int): primeiro número a ser somado
        num2 (int): segundo número a ser somado
        num3 (int): terceiro número a ser somado
    
    Returns:
        soma(int): o valor da soma dos 3 números dados como argumentos
    '''
    return num1+num2+num3


soma = soma_3(2,2,2)
print(soma)